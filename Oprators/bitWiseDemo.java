public class bitWiseDemo {
    public static void main(String args[])
    {
        int x=10 , y;

        y=x<<2;

        // Here if you left shifted  X by N than it will multiply by 2 power N

        System.out.println(y);


        /*--------------------------*/

        int a=10;

        float b;

        b=a>>2;

        // Here if you right shifted A by N than it will divide by 2 power of N

        System.out.println(b);
    }
}
