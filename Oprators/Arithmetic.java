
public class Arithmetic {
    public static void main(String args[])
    {
     
        // Integer Type
          byte b=10;
          short s=5;
  
          int sum=s+b;

        System.out.println(sum);

        //Float Type
        float f=12.5f;
        long l=123;

        float mul=f*l;

        System.out.println(mul);

        //double type
        float F=12.5f;
        double d=123;

        double div=F/d;

        System.out.println(div);

        //Division is High Precedence


    }
}
